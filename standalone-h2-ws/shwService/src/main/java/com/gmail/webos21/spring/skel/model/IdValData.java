package com.gmail.webos21.spring.skel.model;

import javax.xml.bind.annotation.XmlRootElement;

import com.gmail.webos21.spring.skel.db.domain.IdVal;

@XmlRootElement(name = "data")
public class IdValData extends AbstractData {

	private Long id;

	private String value;

	public IdValData(IdVal tu) {
		this.id = tu.getId();
		this.value = tu.getValue();
	}

	public Long getId() {
		return id;
	}

	public void setId(Long id) {
		this.id = id;
	}

	public String getValue() {
		return value;
	}

	public void setValue(String value) {
		this.value = value;
	}

}
